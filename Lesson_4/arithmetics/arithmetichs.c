#include <stdio.h>
#include <stdlib.h>

                            /*
                                Arithmetic Operators

                                +   -   *   /   %   ++  --

                                % => mode
                            */

int main()
{
    printf("15+20 = %d",15+20);
    printf("\n15-20 = %d",15-20);
    printf("\n15*20 = %d",15*20);
    float divide = 21/20;
    printf("\n21/20 = %f",divide);
    printf("\n24 mode 20 = %d",24%20);

    return 0;
}